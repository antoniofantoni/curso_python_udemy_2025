''''
==: Igualdade.
!=: Diferente.
>: Maior que.
<: Menor que.
>=: Maior ou igual a.
<=: Menor ou igual a.

'''

x = 5
y = 3
z = 5

# Igualdade
print(x == z)  # True

# Diferente
print(x != y)  # True

# Maior que
print(x > y)  # True

# Menor que
print(x < z)  # False

# Maior ou igual a
print(x >= z)  # True

# Menor ou igual a
print(y <= x)  # True